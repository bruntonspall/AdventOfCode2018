import unittest
import puzzle


class TestBasic(unittest.TestCase):
    def test_coord(self):
        #    x  y  dx dy
        c = (5, 5, -1, 1)
        self.assertEqual((4, 6, -1, 1), puzzle.coord_at(1, c))
        self.assertEqual((3, 7, -1, 1), puzzle.coord_at(2, c))
        self.assertEqual((0, 10, -1, 1), puzzle.coord_at(5, c))

    def test_parse(self):
        self.assertEqual([(9, 1, 0, 2)], puzzle.parse(["position=< 9,  1> velocity=< 0,  2>"]))

